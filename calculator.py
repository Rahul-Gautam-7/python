number1 = int(input("Enter a first number : "))
number2  = int(input("Enter a second number : "))

if number1 and number2:
    print(f"Sum of the number : {number1+number2}")
    print(f"Difference of the number : {number1-number2}")
    print(f"Product of the number : {number1*number2}")
    print(f"Division of the number : {number1/number2}")